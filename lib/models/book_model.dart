class BookModel {
  final String title;
  final String author;
  final String price;
  final String image;
  final String rating;
  final String aboutAuthor;
  final String overview;
  final String category;

  BookModel({
    required this.title,
    required this.author,
    required this.price,
    required this.image,
    required this.rating,
    required this.aboutAuthor,
    required this.overview,
    required this.category,
  });
}
